    private void btningresoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btningresoActionPerformed
    String email = txtemail.getText().trim();
    String pass = new String(txtpass.getPassword()).trim();

    if (email.isEmpty() || pass.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Debe ingresar email y contraseña.");
        return;
    }

    LoginNeg login = new LoginNeg();
    LoginNeg.UsuarioLogin usuario = login.autenticar(email, pass);

    if (usuario == null) {
        JOptionPane.showMessageDialog(this, "Credenciales inválidas.");
    } else {
        JOptionPane.showMessageDialog(this, "Bienvenido " + usuario.nombre + " (" + usuario.rol + ")");

        switch (usuario.rol.toUpperCase()) {
            case "ADMIN":
                new Admin(usuario).setVisible(true);
                break;
            case "MANAGER":
                new Manager(usuario).setVisible(true);
                break;
            default:
                JOptionPane.showMessageDialog(this, "Este sistema es solo para administradores o encargados.");
                return;
        }

        this.dispose(); // cerrar login
    }
    }//GEN-LAST:event_btningresoActionPerformed

